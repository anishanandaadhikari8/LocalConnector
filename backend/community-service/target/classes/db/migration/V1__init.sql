CREATE TABLE amenity (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  location VARCHAR(255),
  capacity INT,
  open_time TIME,
  close_time TIME,
  requires_approval BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE announcement (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  body CLOB NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
  created_by_user_id BIGINT NOT NULL,
  pinned BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE booking (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  amenity_id BIGINT NOT NULL,
  user_id BIGINT NOT NULL,
  start_at TIMESTAMP WITH TIME ZONE NOT NULL,
  end_at TIMESTAMP WITH TIME ZONE NOT NULL,
  status VARCHAR(20) NOT NULL,
  CONSTRAINT fk_booking_amenity FOREIGN KEY (amenity_id) REFERENCES amenity(id)
);

CREATE TABLE incident (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reporter_user_id BIGINT NOT NULL,
  type VARCHAR(32) NOT NULL,
  description CLOB NOT NULL,
  location_hint VARCHAR(255),
  reported_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
  severity INT,
  status VARCHAR(20) NOT NULL
);

CREATE INDEX idx_booking_amenity_time_status ON booking(amenity_id, start_at, end_at, status);
CREATE INDEX idx_incident_status ON incident(status);
CREATE INDEX idx_announcement_pinned_created ON announcement(pinned, created_at);

